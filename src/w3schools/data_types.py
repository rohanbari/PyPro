# Learning about data types in Python

var1 = "Sussy Baka"  # string
var2 = 420  # integer
var3 = 123.4  # float
var4 = 4j  # complex
var5 = ['Apple', 'Banana']  # list
var6 = ('Apple', 'Banana')  # tuple
var7 = range(10)  # range
var8 = {"name": "Begula", "age": "50"}  # dict
var9 = {"Bugatti", "Ferrari", "Lamborghini"}  # set
var10 = frozenset({"Bugatti", "Ferrari", "Lamborghini"})  # frozenset
var11 = False  # bool
var12 = b"Sussy Baka"  # byte
var13 = bytearray(16)  # bytearray
var14 = memoryview(bytes(16))  # memoryview
var15 = None

print(var1, var2, var3, var4, var5, var6, var7, var8,
      var9, var10, var11, var12, var13, var14, var15, sep="\n")
